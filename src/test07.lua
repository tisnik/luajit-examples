-- inicializace promennych konstantami
local a = 1
local b = 2
local c = 3

-- inicializace promennych s vyuzitim aritmetickych vyrazu
local x = a + b + c
local y = a - b * c
local z = a * (b + c)
local w = (a + b) * (b + c)
local q = (a % b) / (b % c)



-- tisk hodnot vsech promennych
print(a)
print(b)
print(c)
print(x)
print(y)
print(z)
print(w)
print(q)



