-- inicializace promennych numerickymi konstantami
local a = 1
local b = 2



-- inicializace promennych s vyuzitim relacnich vyrazu
local x1 = a == b
local x2 = a ~= b
local x3 = a <  b
local x4 = a <= b
local x5 = a >  b
local x6 = a >= b

-- tisk hodnot vsech promennych
print(a)
print(b)

print()

print(x1)
print(x2)
print(x3)
print(x4)
print(x5)
print(x6)



