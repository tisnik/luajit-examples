-- inicializace promenne konstantou
local a = 1



-- inicializace promennych s vyuzitim relacnich vyrazu
local x1 = a == 10
local x2 = a ~= 10
local x3 = a <  10
local x4 = a <= 10
local x5 = a >  10
local x6 = a >= 10

-- tisk hodnot vsech promennych
print(a)

print()

print(x1)
print(x2)
print(x3)
print(x4)
print(x5)
print(x6)



